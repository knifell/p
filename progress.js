    function updatePageContent(lang) {
        // Обновление статических элементов
        document.title = translations[lang][document.title] || document.title;
        document.querySelector('.logo-text').textContent = translations[lang]['КАРА-ГУЗ'];
        document.querySelector('.section-title').textContent = translations[lang]['План по социально-экономическому развитию на 2025-2030 годы'];
        document.getElementById('progress-search').placeholder = translations[lang]['Поиск по планам...'];
        document.querySelector('.footer-content p').textContent = translations[lang]['2025 Кара-Гуз айыл аймагы. Все права защищены.'];

        // Обновление кнопок фильтров
        document.querySelector('[data-filter="all"]').textContent = translations[lang]['Все'];
        document.querySelector('[data-filter="экономическое"]').textContent = translations[lang]['Экономическое'];
        document.querySelector('[data-filter="социальное"]').textContent = translations[lang]['Социальное'];
        document.querySelector('[data-filter="инфраструктура"]').textContent = translations[lang]['Инфраструктура'];
        document.querySelector('[data-filter="агропромышленный"]').textContent = translations[lang]['Агропромышленный'];
        document.querySelector('[data-filter="туризм"]').textContent = translations[lang]['Туризм'];

        // Обновление заголовков секций
        document.querySelectorAll('.progress-section-title').forEach(heading => {
            const originalText = heading.textContent.replace(/^4\.1\.\d\.\s*/, '');
            const prefix = heading.textContent.match(/^4\.1\.\d\.\s*/)?.[0] || '';
            const newText = translations[lang][originalText] || originalText;
            heading.textContent = prefix + newText;
        });

        // Обновление всех карточек
        progressItems.forEach(item => {
            // Название проекта
            const h3 = item.querySelector('h3');
            if (h3) {
                h3.textContent = translations[lang][h3.textContent.trim()] || h3.textContent;
            }

            // Ожидаемый результат
            const expectedResultP = item.querySelector('p:nth-of-type(1)');
            if (expectedResultP) {
                const originalText = expectedResultP.textContent.replace('Ожидаемый результат:', '').trim();
                const newText = translations[lang][originalText] || originalText;
                expectedResultP.innerHTML = `<strong>${translations[lang]['Ожидаемый результат:']}</strong> ${newText}`;
            }

            // Ответственный
            const responsibleP = item.querySelector('p:nth-of-type(2)');
            if (responsibleP) {
                const originalText = responsibleP.textContent.replace('Ответственный:', '').trim();
                const newText = translations[lang][originalText] || originalText;
                responsibleP.innerHTML = `<strong>${translations[lang]['Ответственный:']}</strong> ${newText}`;
            }

            // Бюджет
            const budgetLabels = item.querySelectorAll('.budget-info .label');
            budgetLabels.forEach(label => {
                label.textContent = translations[lang][label.textContent.trim()] || label.textContent;
            });
        });
    }

