    function filterItems(category) {
        progressItems.forEach(item => {
            const isVisible = category === 'all' || item.getAttribute('data-category') === category;
            item.style.display = isVisible ? 'block' : 'none';
        });

        categoryHeadings.forEach(heading => {
            const headingCategory = heading.getAttribute('data-category-heading');
            const hasVisibleItems = Array.from(progressItems).some(item =>
                item.style.display !== 'none' && item.getAttribute('data-category') === headingCategory
            );
            heading.style.display = (category === 'all' || headingCategory === category) && hasVisibleItems ? 'block' : 'none';
        });
    }

    function searchItems(query) {
        const lowerCaseQuery = query.toLowerCase();
        progressItems.forEach(item => {
            const textContent = item.textContent.toLowerCase();
            const isVisible = textContent.includes(lowerCaseQuery);
            item.style.display = isVisible ? 'block' : 'none';
        });

        categoryHeadings.forEach(heading => {
            const headingCategory = heading.getAttribute('data-category-heading');
            const hasVisibleItems = Array.from(progressItems).some(item =>
                item.style.display !== 'none' && item.getAttribute('data-category') === headingCategory
            );
            heading.style.display = hasVisibleItems ? 'block' : 'none';
        });
    }

